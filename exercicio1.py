def multiplas_operacoes(a,b):

    soma=float(a)+float(b)
    subtracao=float(a)-float(b)
    multiplicacao=float(a)*float(b)


    if float(b)==0 or float(a)==0:
        divisao=0
    else:
        divisao=float(a)/float(b)

    return (soma,subtracao,multiplicacao,divisao)



        
  









